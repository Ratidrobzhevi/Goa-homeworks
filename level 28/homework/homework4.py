# .lower() — ყველა ასოს გადაიყვანს პატარა ასოებად
text = "HELLO"
print(text.lower())  # შედეგი: "hello"

# .upper() — ყველა ასოს გადაიყვანს დიდ ასოებად
text = "hello"
print(text.upper())  # შედეგი: "HELLO"

# .capitalize() — მხოლოდ პირველი ასო იქნება დიდი, დანარჩენი — პატარა
text = "hello world"
print(text.capitalize())  # შედეგი: "Hello world"

